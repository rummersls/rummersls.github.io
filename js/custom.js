var numberOfImagesInDirectory = 20;


var bgImageArray = [],
base = "images/gallery/",
secs = 6;

for (let i = 1; i < numberOfImagesInDirectory; i++) {
    new Image().src = base + i + ".jpg"; 
    bgImageArray.push(i + ".jpg");
}


// Custom Rummers Background
function backgroundSequence() {
	window.clearTimeout();
	var k = 0;
	for (i = 0; i < bgImageArray.length; i++) {
		setTimeout(function(){ 
			document.body.style.background = "url(" + base + bgImageArray[k] + ") no-repeat center center fixed";
			document.body.style.backgroundSize ="cover";
		if ((k + 1) === bgImageArray.length) { setTimeout(function() { backgroundSequence() }, (secs * 1000))} else { k++; }			
		}, (secs * 1000) * i)	
	}
}










